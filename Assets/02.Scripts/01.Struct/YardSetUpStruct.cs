public struct YardSetUp
{
    public int Dong;
    public int DxOffset;
    public int DxMax;
    public int DyOffset;
    public int DyMax;
    public int Height;
    public int DxSpacing;

    public YardSetUp(int dong, int dxOffset, int dxMax, int dyOffset, int dyMax, int height, int dxSpacing)
    {
        Dong = dong;
        DxOffset = dxOffset;
        DxMax = dxMax;
        DyOffset = dyOffset;
        DyMax = dyMax;
        Height = height;
        DxSpacing = dxSpacing;
    }
}
