public struct YardSetUp
{
    public int Dong;
    public int DxOffset;
    public int DxMax;
    public int DyOffset;
    public int DyMax;

    public YardSetUp(int dong, int dxOffset, int dxMax, int dyOffset, int dyMax)
    {
        Dong = dong;
        DxOffset = dxOffset;
        DxMax = dxMax;
        DyOffset = dyOffset;
        DyMax = dyMax;
    }
}
